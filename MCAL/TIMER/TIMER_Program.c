void TIMER2_VidInit(void)
{
	/*Select the suitable Prescaler */
		TCCR2&=0xF8;
		TCCR2|=TIMER2_SET_PRESCALER;
		/*Mode Select*/
	#if TIMER2_SET_MODE == TIMER2_NORMAL_MODE
		CLR_BIT(TCCR2,6);
		CLR_BIT(TCCR2,3);
		/*Timer Overflow Interrupt Enable*/
		SET_BIT(TIMSK,6);

	#elif TIMER2_SET_MODE == TIMER2_CTC_MODE
		CLR_BIT(TCCR2,6);
		SET_BIT(TCCR2,3);
	#if TIMER2_SET_CTC_INTERRUPT == TIMER2_CTC_INTERRUPT_ENABLED
		SET_BIT(TIMSK,7);
	#elif TIMER2_SET_CTC_INTERRUPT == TIMER2_CTC_INTERRUPT_DISABLED
		CLR_BIT(TIMSK,7);
	#else
	#error "TIMER2 CTC Interrupt Mode is not valid..."
	#endif
	#if TIMER2_SET_OC0_PIN_MODE == TIMER2_OC2_PIN_DISCONNECTED
		CLR_BIT(TCCR2,5);
		CLR_BIT(TCCR2,4);
	#elif TIMER2_SET_OC2_PIN_MODE == TIMER0_OC2_PIN_TOGGLE
		CLR_BIT(TCCR2,5);
		SET_BIT(TCCR2,4);
	#elif TIMER2_SET_OC2_PIN_MODE == TIMER2_OC2_PIN_SET
		SET_BIT(TCCR2,5);
		SET_BIT(TCCR2,4);
	#elif TIMER2_SET_OC2_PIN_MODE == TIMER2_OC2_PIN_CLR
		SET_BIT(TCCR2,5);
		CLR_BIT(TCCR2,4);
	#else
	#error "TIMER2 CTC OC2 Mode is not valid..."
	#endif
	#elif TIMER2_SET_MODE == TIMER2_PHASECORRECT_PWM_MODE
		SET_BIT(TCCR2,6);
		CLR_BIT(TCCR2,3);
		/*select phase correct mode */
	#if TIMER2_SET_PWM_MODE == TIMER2_NUN_INVERTING_PWM
		   SET_BIT(TCCR2,1);
			CLR_BIT(TCCR2,0);
			CLR_BIT(TCCR2,4);
			SET_BIT(TCCR2,5);

	#elif TIMER2_SET_PWM_MODE == TIMER2_INVERTING_PWM
		    SET_BIT(TCCR2,5);
			SET_BIT(TCCR2,4);

	#else
	#error "TIMER2 PWM Mode is not valid..."
	#endif
	#elif TIMER2_SET_MODE == TIMER2_FAST_PWM_MODE
		SET_BIT(TCCR2,6);
		SET_BIT(TCCR2,3);
		/*select TIMER2 FAST PWM_MODE mode */
	#if TIMER2_SET_PWM_MODE == TIMER2_NUN_INVERTING_PWM
		        SET_BIT(TCCR2,1);
				CLR_BIT(TCCR2,0);
				CLR_BIT(TCCR2,4);
				SET_BIT(TCCR2,5);
	#elif TIMER2_SET_PWM_MODE == TIMER2_INVERTING_PWM
				SET_BIT(TCCR2,5);
				SET_BIT(TCCR2,4);


	#else
	#error "TIMER2 PWM Mode is not valid..."
	#endif
	#else
	#error "TIMER2 Mode is not valid..."
	#endif



}
