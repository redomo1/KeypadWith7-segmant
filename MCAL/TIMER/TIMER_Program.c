void __vector_6(void)
{
	if(Flag==0)
		{
			T1=ICR1+Count*65536;
			/*ICU Sense with falling*/
			CLR_BIT(TCCR1B,6);
			Flag=1;
		}
		else if(Flag==1)
		{
			T2=ICR1+Count*65536;
			/*ICU Sense with Rising*/
			SET_BIT(TCCR1B,6);
			Flag=2;
		}
		else if(Flag==2)
		{
			T3=ICR1+Count*65536;
			Flag=3;
		}
}

